def main():
    from app import create_app
    app = create_app("production")

    with app.app_context():
        from app.extensions.database import db
        from app.models.employee_master import EmployeeMaster

        # Ensure tables exist
        db.create_all()
        print(f"Starting seed — {len(EMPLOYEES)} employees to process...")

        imported = skipped = 0
        for code, name in EMPLOYEES:
            if EmployeeMaster.query.filter_by(employee_code=code).first():
                skipped += 1
                continue
            db.session.add(EmployeeMaster(employee_code=code, employee_name=name))
            imported += 1

        db.session.commit()

        total = EmployeeMaster.query.count()
        print("=" * 50)
        print(f"  Imported  : {imported}")
        print(f"  Skipped   : {skipped} (already existed)")
        print(f"  DB total  : {total}")
        print("=" * 50)
        print("Done. Employees can now register at /auth/login")


if __name__ == "__main__":
    main()
